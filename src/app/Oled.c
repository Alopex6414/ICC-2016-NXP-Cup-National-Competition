/*
**Function:int OledCMD(byte Str[],Mod)
**Oled参数输入命令
**Mod=0,Nomal
**Mod=1,Beacon
*/
int OledCMD(byte Str[],byte Mod)
{
  int i=0;
  int Value=0;
  int SectorNo=0;//Flash扇区号
  int ValueTenThousand=0,ValueThousand=0,ValueHundred=0,ValueTen=0,ValueSingle=0;
  
  SectorNo = 255 - SectorTimes++;
  Value = (int)(*(uint32 *)((uint32)((SectorNo)<<11)));//读取Flash存储参数
  
  if(Mod==0)//Nomal
  {
    BitSwitch=2;//参数输入起始位置为首位
  }
  else if(Mod==1)//Beacon
  {
    BitSwitch=4;//参数输入起始位置为首位
  }
  
  EnterFlag=0;                  //输入标志清零
  LCD_CLS();                    //Oled屏幕清屏
  
  while (EnterFlag!=1)          //确定键没有按下
  {
    if(KEY_E_VAL==1)            //'E'确定键按下
    {
      EnterFlag = 1;            //输入标志置位  
      delayms1(Oled_DELAY);     //按压延时
    }
    else if(KEY_Q_VAL==1)       //'Q'取消键按下
    {
      TurnBackFlag = 1;         //返回标志
      delayms1(Oled_DELAY);     //按压延时
      goto RET;
    }
    else if(KEY_L_VAL==1)       //'L'左键按下
    {
      if(Mod==0)//Nomal
      {
        BitSwitch++;
        if(BitSwitch > 2)
        {
          BitSwitch = 0;
        }
      }
      else if(Mod==1)//Beacon
      {
        BitSwitch++;
        if(BitSwitch > 4)
        {
          BitSwitch = 0;
        }
      }
      delayms1(Oled_DELAY);     //按压延时
    }
    else if(KEY_R_VAL==1)       //'R'右键按下
    {
      if(Mod==0)//Nomal
      {
        BitSwitch--;
        if(BitSwitch < 0)
        {
          BitSwitch = 2;
        }
      }
      else if(Mod==1)//Beacon
      {
        BitSwitch--;
        if(BitSwitch < 0)
        {
          BitSwitch = 4;
        }
      }
      delayms1(Oled_DELAY);//按压延时
    }
    else if(KEY_U_VAL==1)       //'U'上键按下
    {
      ValueTenThousand = Value / 10000;
      ValueThousand = (Value % 10000) / 1000;
      ValueHundred = (Value % 1000) / 100;
      ValueTen = (Value % 100) / 10;
      ValueSingle = Value % 10;
        
      if(BitSwitch == 4)
      {
        BitValue = ValueTenThousand;
      }
      else if(BitSwitch == 3)
      {
        BitValue = ValueThousand;
      }
      else if(BitSwitch == 2)
      {
        BitValue = ValueHundred;
      }
      else if(BitSwitch == 1)
      {
        BitValue = ValueTen;
      }
      else if(BitSwitch == 0)
      {
        BitValue = ValueSingle;
      }
        
      if(Mod==0)//Nomal
      {
        BitValue++;
        if(BitValue > 9)
        {
          BitValue = 0;
        }
      }
      else if(Mod==1)//Beacon
      {
        BitValue++;
        if(BitValue > 1)
        {
          BitValue = 0;
        }
      }
        
      if(BitSwitch == 4)
      {
        Value = BitValue * 10000 + ValueThousand * 1000 + ValueHundred * 100 + ValueTen * 10 + ValueSingle;
      }
      else if(BitSwitch == 3)
      {
        Value = ValueTenThousand * 10000 + BitValue * 1000 + ValueHundred * 100 + ValueTen * 10 + ValueSingle;
      }
      else if(BitSwitch == 2)
      {
        Value = ValueTenThousand * 10000 + ValueThousand * 1000 + BitValue * 100 + ValueTen * 10 + ValueSingle;
      }
      else if(BitSwitch == 1)
      {
        Value = ValueTenThousand * 10000 + ValueThousand * 1000 + ValueHundred * 100 + BitValue * 10 + ValueSingle;
      }
      else if(BitSwitch == 0)
      {
        Value = ValueTenThousand * 10000 + ValueThousand * 1000 + ValueHundred * 100 + ValueTen * 10 + BitValue;
      }
        
      delayms1(Oled_DELAY);//按压延时
    }
    else if(KEY_D_VAL==1)       //'D'下键按下
    {
      ValueTenThousand = Value / 10000;
      ValueThousand = (Value % 10000) / 1000;
      ValueHundred = (Value % 1000) / 100;
      ValueTen = (Value % 100) / 10;
      ValueSingle = Value % 10;
        
      if(BitSwitch == 4)
      {
        BitValue = ValueTenThousand;
      }
      else if(BitSwitch == 3)
      {
        BitValue = ValueThousand;
      }
      else if(BitSwitch == 2)
      {
        BitValue = ValueHundred;
      }
      else if(BitSwitch == 1)
      {
        BitValue = ValueTen;
      }
      else if(BitSwitch == 0)
      {
        BitValue = ValueSingle;
      }
        
      if(Mod==0)//Normal
      {
        BitValue--;
        if(BitValue < 0)
        {
          BitValue = 9;
        }
      }
      else if(Mod==1)//Beacon
      {
        BitValue--;
        if(BitValue < 0)
        {
          BitValue = 1;
        }
      }
        
      if(BitSwitch == 4)
      {
        Value = BitValue * 10000 + ValueThousand * 1000 + ValueHundred * 100 + ValueTen * 10 + ValueSingle;
      }
      else if(BitSwitch == 3)
      {
        Value = ValueTenThousand * 10000 + BitValue * 1000 + ValueHundred * 100 + ValueTen * 10 + ValueSingle;
      }
      else if(BitSwitch == 2)
      {
        Value = ValueTenThousand * 10000 + ValueThousand * 1000 + BitValue * 100 + ValueTen * 10 + ValueSingle;
      }
      else if(BitSwitch == 1)
      {
        Value = ValueTenThousand * 10000 + ValueThousand * 1000 + ValueHundred * 100 + BitValue * 10 + ValueSingle;
      }
      else if(BitSwitch == 0)
      {
        Value = ValueTenThousand * 10000 + ValueThousand * 1000 + ValueHundred * 100 + ValueTen * 10 + BitValue;
      }
        
      delayms1(Oled_DELAY);//按压延时
    }
    
    LCD_P12x24Str(0,0,Str);                             //参数名称显示
    ParameterInput(64,5,"_",Value,BitSwitch,Mod);       //默认参数值显示
  }
  
  Value = (uint32)Value;                        //Flash参数存储
  flash_erase_sector(SectorNo);
  flash_write(SectorNo,0,Value);
  
  RET:return Value;
}


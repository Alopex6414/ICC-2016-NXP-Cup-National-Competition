      {
        SwitchRow--;                                    //行计数减一
      }
      
      if(SwitchRow == 0 && SwitchCol == 0)              //如果行计数等于0,选择列为0
      {
        SwitchRow = 3;                                  //跳转到第3行
        SwitchCol = 1;                                  //跳转到第1列
      }
      else if(SwitchRow == 0 && SwitchCol == 1)         //如果行计数等于0,选择列为1
      {
        SwitchRow = 3;                                  //跳转到第3行
        SwitchCol = 0;                                  //跳转到第0列
      }
      
      if(SwitchCol == 0 && SwitchRow != 3)
      {
        LCD_P8x16Str(56,SwitchRowOld*2," ");             //清除上次行.
        LCD_P8x16Str(56,SwitchRow*2,"o");                //当前行.
      }
      else if(SwitchCol == 0 && SwitchRow == 3)
      {
        LCD_P8x16Str(120,2," ");                         //清除上次行.
        LCD_P8x16Str(56,6,"o");                          //当前行.
      }
      if(SwitchCol == 1 && SwitchRow != 3)
      {
        LCD_P8x16Str(120,SwitchRowOld*2," ");            //清除上次行.
        LCD_P8x16Str(120,SwitchRow*2,"o");               //当前行.
      }
      else if(SwitchCol == 1 && SwitchRow == 3)
      {
        LCD_P8x16Str(56,2," ");                          //清除上次行.
        LCD_P8x16Str(120,6,"o");                         //当前行.
      }
        
      ChoiceStatus=1;                                   //选择状态置1

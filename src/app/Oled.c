/**
**Function: void Image_Dis(void)
**Description: 显示信标图像
**Calls:   无
**Called By: isr.c
**Table Accessed: 无
**Table Updated: 无
**Input: 无
**Output: 无
**Return: 无
**Others: 摄像头图像显示行未压缩,列折半压缩
          注意摄像头图像ROW和COL,不要超过Max_Row和Max_Column
**/
void Image_Dis(void)
{
    int i=0,k=0,j=0,m=0;
    int p=0,q=0;
    byte DATA=0;
    byte DATA_Now=0;
    long int date = 0;
    int HalfCol=0;
    static int LastCol=0;
    unsigned char Image_dis[Max_Row][COL] = {0};  
    unsigned char *PTemp = &guImgBuff[0][0];           
    unsigned char *PEnd = &guImgBuff[0][0];
    
    for(i = 0;i< Max_Row;i++)   //游程编码数组转换为原图 '0'黑,'1'白   
    {                                                                                    
      PTemp = &guImgBuff[i][0];                     
      PEnd = &guImgBuff[i][NUM-1];                
      for(m = 0;PTemp < PEnd && *PTemp != 0 && *(PTemp+1) != 0;PTemp+=2)
      {
        for(k = *PTemp;k < *(PTemp+1);k++)       
        {                                  
          Image_dis[i][k] = 1;
        }
      }                  
    }
    
    for(i=0; i<Max_Row; i++)    //图像列压缩
    {
      for(j=0; j<COL/2; j++)
      {
        Image_dis[i][j*2] |= Image_dis[i][j*2+1];
      }
    }
    
    if(KEY_D_VAL==1)        //'D'键按下显示坐标    
    {
      if(guImageStaut == 0)
      {
        guImageStaut = 1;   //中心虚线图像
      }
      else
      {
        guImageStaut = 0;   //正常图像显示
      }
      delayms1(Page_DELAY);//按压延时
    }
    
    //显示中心点(虚线)
    if((ROW - giBeacon_Row) < Max_Row)//行列号显示
    {
      p=(ROW - giBeacon_Row)/8;
      q=(ROW - giBeacon_Row)%8;
      DATA = 0x01<<q;
    }

    HalfCol = giBeacon_Col/2;
    
    if(HalfCol == COL/2)
    {
      HalfCol = COL/2 - 1;
    }
    
    for(i=0; i<8; i++)  //图像8bit合成
    {    
      LCD_Set_Pos(0,i);//设置行扫描起点
     
      if(guImageStaut == 1)
      {
        if(i == p)
        {
          for(m=0; m<COL/2; m++)
          {
            if(m%4==0)
            {
              DATA_Now = DATA;
            }
            else if(m==HalfCol)
            {
              DATA_Now = 0x11;
            }
            else
            {
              DATA_Now = 0;
            }
            
            date = Image_dis[i*8][m*2]
                   +Image_dis[i*8+1][m*2]*2
                   +Image_dis[i*8+2][m*2]*4
                   +Image_dis[i*8+3][m*2]*8
                   +Image_dis[i*8+4][m*2]*16
                   +Image_dis[i*8+5][m*2]*32
                   +Image_dis[i*8+6][m*2]*64
                   +Image_dis[i*8+7][m*2]*128;
            date|=DATA_Now;
            LCD_WrDat(date);              
           }
        }
        else
        {
          for(m=0; m<COL/2; m++)
          {
            date = Image_dis[i*8][m*2]
                   +Image_dis[i*8+1][m*2]*2
                   +Image_dis[i*8+2][m*2]*4
                   +Image_dis[i*8+3][m*2]*8
                   +Image_dis[i*8+4][m*2]*16
                   +Image_dis[i*8+5][m*2]*32
                   +Image_dis[i*8+6][m*2]*64
                   +Image_dis[i*8+7][m*2]*128;         
            LCD_WrDat(date);              
           }
        }
        LCD_Set_Pos(LastCol,i);//信标列显示
        LCD_WrDat(0x00);
        LCD_Set_Pos(HalfCol,i);
        LCD_WrDat(0x11);
      }
      
      else
      {
        for(m=0; m<COL/2; m++)
        {
          date = Image_dis[i*8][m*2]
                +Image_dis[i*8+1][m*2]*2
                +Image_dis[i*8+2][m*2]*4
                +Image_dis[i*8+3][m*2]*8
                +Image_dis[i*8+4][m*2]*16
                +Image_dis[i*8+5][m*2]*32
                +Image_dis[i*8+6][m*2]*64
                +Image_dis[i*8+7][m*2]*128;         
          LCD_WrDat(date);              
         }
      }
      
    }
    
    LastCol = HalfCol;   //记录上次列号
    
    //标记图像行列号
    LCD_P6x8Str(COL/2-10,4,"ROW");
    LCD_P6x8Str(COL/2-10,6,"COL");
    LCD_P6x8Parameter(COL/2-10,5,"",giBeacon_Row);
    LCD_P6x8Parameter(COL/2-10,7,"",giBeacon_Col);
    
}

